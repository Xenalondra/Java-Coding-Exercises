    private boolean gameOver() {
        String result;
        if (wordList.isEmpty()) {
            result = "üéâ Congratulations! üéâ\n"+
                            "\nYou answered all the scrambled words." +
                            "\nüìù Total number of words: " + correctWords +
                            "\nüèÜ Total Score: " + score;
            JOptionPane.showMessageDialog(null, result);
            return true;
        }
        else if (hp == 0 || score < 0) {
            if (score < 0) {
                score = 0;
            }
            result = "üö© GAME OVER! üö©\n"+
                            "\nüìù Total number of words: " + correctWords +
                            "\nü•á Total Score: " + score;
            JOptionPane.showMessageDialog(null, result);
            return true;
        }
        return false;
    }
    
    public final void startTimer() {
        timer = new Timer(1000, (ActionEvent e) -> {
            counter--;
            if (counter == 0) {
                hp -= 5;
                startGame();
                restartTimer();
                pblifeline.setValue(hp);
            }
            lblTimer.setText(String.valueOf(counter));
        });
        timer.start();
    }
    
    public void restartTimer(){
        counter = 15;
        lblTimer.setText(String.valueOf(counter));
        timer.restart();
    }
    
    public void stopTimer(){
        counter = 0;
        lblTimer.setText(String.valueOf(counter));
        timer.stop();
    }
